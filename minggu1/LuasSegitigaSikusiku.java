package week1;
public class LuasSegitigaSikusiku {
    public static void main(String[] args) {
        // Deklarasi variabel untuk alas dan tinggi segitiga
        double alas = 6.0;
        double tinggi = 8.0;
        
        // Menghitung luas segitiga siku-siku
        double luas = (alas * tinggi) / 2;
        
        // Menampilkan hasil luas segitiga siku-siku
        System.out.println("Luas segitiga siku-siku dengan alas " + alas + " dan tinggi " + tinggi + " adalah: " + luas);
    }
}
